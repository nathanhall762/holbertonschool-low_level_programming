#include "3-calc.h"

/**
* op_add - adds 2 ints
* @a: int 1
* @b: int 2
*
* Return: a+b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtract 2 ints
* @a: int 1
* @b: int 2
*
* Return: a-b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiply 2 ints
* @a: int 1
* @b: int 2
*
* Return: a*b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - div 2 ints
* @a: int 1
* @b: int 2
*
* Return: a/b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - modulos 2 ints
* @a: int 1
* @b: int 2
*
* Return: a%b
*/

int op_mod(int a, int b)
{
	return (a % b);
}

